print("Implicit Type Casting Example :-")
print("Integer converting to Float numbers automatically by Python!")
a = 2 # a is a integer
b = 4.5 # b is a float

print(a + b) # result will be float

print()

print("Explicit Type Casting Example :-")
print("We are converting String to Integer Manually!")
a = "5" # a is a string
b = "10" # b is a string

print(int(a) + int(b)) # result will be integer



