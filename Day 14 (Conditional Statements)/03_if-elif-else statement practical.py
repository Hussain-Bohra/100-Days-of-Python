# if-elif-else statement example :-

# Syntax :-
# if(condition1):
#     statement

# elif(condition2):
#     statement

# else:
#     statement


# Example :-
number = int(input("Enter Number: "))

if(number == 25):
    print(number,"is Equals to 25")

elif(number > 20):
    print(number,"is Greater than 20")

else:
    print(number,"is Less Than 20")

