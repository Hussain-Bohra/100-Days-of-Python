# We are Repeating our Exercise now by taking user input and perform arithmetic operation.

num1 = int(input("Enter Num1: "))
num2 = int(input("Enter Num2: "))

print()

print("Addition :-")
print(num1,"+",num2, "=", num1 + num2)
print()

print("Subtraction :-")
print(num1,"-",num2, "=", num1 - num2)
print()

print("Multiplication :-")
print(num1,"*",num2, "=", num1 * num2)
print()

print("Division :-")
print(num1,"/",num2, "=", num1 / num2)
print()

print("Floor Division :-")
print(num1,"//",num2, "=", num1 // num2)
print()

print("Exponential :-")
print(num1,"**",num2, "=", num1 ** num2) 
print()

print("Arithmetic Operation Done Successfully!")