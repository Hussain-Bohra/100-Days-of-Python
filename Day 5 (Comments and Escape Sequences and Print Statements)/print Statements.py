# We know print() is used to display output on console screen.
# There are parameters that we can use with print statement.

# Examples :-
# 1. sep parameter - by default it uses "space".
print("Incredible","Hulk",sep='~')
print("Captain","America",sep='#')
print("Iron","Man",sep='-')

# 2. end parameter - by default it uses "next line".
print("Hello", end="awesome\n")
print("Friend")